    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        JTextField[] tfTT = {tfMaLSP, tfTenLSP, tfMoTa};
        for (JTextField tf : tfTT) {
            if (tf.getText().matches("") || tf.getText().matches("\\s+")) {
                JOptionPane.showMessageDialog(this, "Điền không đủ thông tin!");
                return;
            }
        }
        if (lspCtrl.add( tfMaLSP.getText(), tfTenLSP.getText(), tfMoTa.getText())) {
            getData();
            JOptionPane.showMessageDialog(this, "Cập nhật dữ liệu thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thất bại!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (tbLoaiSanPham.getSelectedRow() != -1) {
            if (lspCtrl.update(tfMaLSP.getText(), tfTenLSP.getText(), tfMoTa.getText())) {
                getData();
                JOptionPane.showMessageDialog(this, "Cập nhật dữ liệu thành công!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chọn loại sản phẩm cần sửa");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tbLoaiSanPham.getSelectedRow() != -1) {
            if (lspCtrl.delete(tfMaLSP.getText())) {
                getData();
                JOptionPane.showMessageDialog(this, "Cập nhật dữ liệu thành công!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Chọn loại sản phẩm cần sửa");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbLoaiSanPham.getModel();

        ArrayList<LoaiSanPham> dssp = lspCtrl.search(tfTimKiem.getText().trim());
        if (dssp.size() > 0) {
            while (model.getRowCount() > 0) {
                model.removeRow(0);
            }
            for (LoaiSanPham sp : dssp) {
                model.addRow(sp.toArray());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy loai sản phẩm " + tfTimKiem.getText().trim());
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void tfTimKiemKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfTimKiemKeyPressed
        if (evt.getKeyCode() == evt.VK_ENTER) {
            DefaultTableModel model = (DefaultTableModel) tbLoaiSanPham.getModel();

            ArrayList<LoaiSanPham> dssp = lspCtrl.search(tfTimKiem.getText().trim());
            if (dssp.size() > 0) {
                while (model.getRowCount() > 0) {
                    model.removeRow(0);
                }
                for (LoaiSanPham sp : dssp) {
                    model.addRow(sp.toArray());
                }
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy loai sản phẩm " + tfTimKiem.getText().trim());
            }

        }
    }//GEN-LAST:event_tfTimKiemKeyPressed

