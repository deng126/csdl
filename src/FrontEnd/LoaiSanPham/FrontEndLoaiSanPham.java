    private void getData() {
        DefaultTableModel model = (DefaultTableModel) tbLoaiSanPham.getModel();
        // Xóa dữ liệu cũ
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        //Cập nhật dữ liệu mới
        for (LoaiSanPham lsp : lspCtrl.readDB()) {
            model.addRow(lsp.toArray());
        }
    }

